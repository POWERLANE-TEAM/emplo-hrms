        <h4>Purpose</h4>
        <p>This policy outlines the procedures for the separation of employees from the company, ensuring compliance
            with Philippine labor laws.</p>

        <h4>Types of Separation</h4>
        <ol>
            <li><strong>Voluntary Resignation</strong><br>
                Employees who wish to resign must submit a written notice at least 30 days in advance, as required by
                the Labor Code of the Philippines (Republic Act No. 8188).
            </li>
            <li><strong>Involuntary Termination</strong><br>
                The company reserves the right to terminate employment due to just causes (e.g., serious misconduct,
                neglect of duty) or authorized causes (e.g., redundancy, retrenchment).
            </li>
            <li><strong>End of Contract</strong><br>
                For employees on fixed-term contracts, termination occurs at the end of the contract period, unless
                otherwise agreed upon.
            </li>
        </ol>

        <h4>Notice Requirements</h4>
        <p>In compliance with the Labor Code, employees must give at least 30 daysâ€™ notice for voluntary resignation.
            The employer must also provide notice of termination, depending on the cause, as specified in the law.</p>

        <h4>Final Pay and Clearance</h4>
        <p>Upon separation, employees are entitled to their final pay, which includes:</p>
        <ul>
            <li>Unpaid wages</li>
            <li>Unused leave credits (if applicable)</li>
            <li>Separation pay (if applicable)</li>
        </ul>
        <p>The employee must complete the clearance process, returning all company property before final pay is
            released.</p>

        <h4>Separation Pay</h4>
        <p>In cases of involuntary termination due to authorized causes (e.g., redundancy, retrenchment), employees are
            entitled to separation pay under the provisions of the Labor Code.</p>

        <h4>Compliance with Legal Requirements</h4>
        <ul>
            <li><strong>Republic Act No. 8188 (Labor Code of the Philippines):</strong> Requires proper notice and
                payment of final wages and benefits.</li>
            <li><strong>Social Security System (SSS) Compliance:</strong> Employers must remit the final contributions
                to SSS, PhilHealth, and Pag-IBIG, as applicable.</li>
        </ul>

        <h4>Dispute Resolution</h4>
        <p>Any disputes arising from separation shall be resolved in accordance with the provisions of the Labor Code
            and through available grievance mechanisms or the Department of Labor and Employment (DOLE).</p>
