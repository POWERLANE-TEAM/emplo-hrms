        <h4>Purpose</h4>
        <p>The purpose of this policy is to establish clear guidelines and procedures for employees of Powerlane
            Resources, Inc. (“Company,” “we,” “us,” or “our”) when submitting requests for leave. The policy ensures
            that leave requests are handled efficiently, fairly, and in compliance with legal requirements. It aims to
            balance employees' needs for time off with the operational needs of the organization.</p>

        <h4>Scope</h4>
        <p>This policy applies to all employees of Powerlane Resources, Inc. who are entitled to take leave in
            accordance with the Company’s leave policies, including but not limited to:</p>
        <ul>
            <li>Sick Leave</li>
            <li>Vacation Leave</li>
            <li>Emergency Leave</li>
            <li>Maternity/Paternity Leave</li>
            <li>Special Leave (e.g., marriage, bereavement)</li>
        </ul>

        <h4>Types of Leave</h4>
        <p>The following are the types of leave available to eligible employees under this policy:</p>
        <ul>
            <li>Sick Leave – Leave taken for personal illness or medical conditions.</li>
            <li>Vacation Leave – Leave taken for personal or recreational purposes.</li>
            <li>Emergency Leave – Leave for urgent or unforeseen situations, such as family emergencies or accidents.
            </li>
            <li>Maternity Leave – Leave for female employees for childbirth and recovery.</li>
            <li>Paternity Leave – Leave for male employees for the birth of a child.</li>
            <li>Special Leave – Other leave types as specified by the Company (e.g., bereavement, marriage).</li>
        </ul>

        <h4>Eligibility</h4>
        <p>Employees must meet the following eligibility criteria to request leave:</p>
        <ul>
            <li>Employees must be regular, probationary, or temporary, depending on the type of leave being requested.
            </li>
            <li>Employees must have sufficient accrued leave credits to cover the requested leave duration (except in
                the case of sick leave, where medical certification may be required).</li>
            <li>Employees must submit leave requests in accordance with the prescribed notice periods and approval
                processes.</li>
        </ul>

        <h4>Leave Request Procedure</h4>
        <p>Employees must follow the procedure outlined below when requesting leave:</p>
        <h5>Request Submission</h5>
        <p>Employees must submit their leave request through the HRMS Employee Self-Service portal, which will
            automatically generate an acknowledgment of the request. The following information must be provided:</p>
        <ul>
            <li>Type of leave (e.g., sick leave, vacation leave).</li>
            <li>Duration of the leave (dates and times).</li>
            <li>Reason for leave (if applicable, especially for sick leave or emergency leave).</li>
            <li>Supporting documentation (e.g., medical certificates, emergency documentation) for specific leave types
                such as sick or emergency leave.</li>
        </ul>

        <h5>Notice Period</h5>
        <p>Employees are required to submit leave requests at least:</p>
        <ul>
            <li>7 days in advance for vacation leave.</li>
            <li>3 days in advance for emergency leave, except in cases of extreme urgency.</li>
            <li>Medical certificate is required for sick leave extending beyond 3 consecutive days.</li>
            <li>Immediate reporting for emergencies such as the death of an immediate family member, in which case the
                employee must inform their supervisor as soon as possible.</li>
        </ul>

        <h5>Approval Process</h5>
        <p>Manager/Supervisor Review: Upon submission, the employee’s immediate supervisor and department head will
            review the leave request based on operational requirements and approve or reject the request.</p>
        <p>HR Confirmation: Once the supervisor and department head approves the leave, the HR department will verify
            the employee's leave balance and process the request. Employees will receive an approval confirmation via
            the HRMS portal and email.</p>
        <p>Special Circumstances: In some cases, additional documentation or information may be requested by the HR
            department or supervisor to process the leave request.</p>

        <h4>Leave Balance Check</h4>
        <p>Employees should ensure that they have enough leave credits available to cover the leave requested. In case
            of insufficient leave balance, employees may request an unpaid leave or use any other applicable paid leave
            (e.g., vacation leave). HR will notify the employee of any leave balance issues during the approval process.
        </p>

        <h4>Approval and Denial of Leave Requests</h4>
        <h5>Approval</h5>
        <p>Leave requests will be approved based on the availability of leave credits, operational needs, and the
            employee’s compliance with the submission process.</p>

        <h5>Denial</h5>
        <p>Leave requests may be denied for the following reasons:</p>
        <ul>
            <li>Insufficient leave balance.</li>
            <li>Failure to provide required documentation (e.g., medical certificates for sick leave).</li>
            <li>Conflicting operational requirements (e.g., overlapping leave requests in a department).</li>
            <li>Failure to comply with the notice period or submission guidelines.</li>
        </ul>

        <h4>Leave Utilization and Reporting</h4>
        <h5>Partial Leave</h5>
        <p>If an employee is requesting a partial day of leave (e.g., half-day sick leave), the request should indicate
            the specific hours and reason. The leave request should be submitted at least 3 hours before the employee's
            work shift starts.</p>

        <h5>Reporting Absence</h5>
        <p>Employees who are unable to attend work due to illness or emergencies should notify their supervisor as soon
            as possible. A formal leave request should follow as soon as possible after the absence.</p>

        <h4>Documentation Requirements</h4>
        <ul>
            <li>Sick Leave: For sick leave lasting more than 3 consecutive days, employees must submit a medical
                certificate from a licensed healthcare provider to support the leave request.</li>
            <li>Emergency Leave: Employees may be required to submit relevant documentation or a written explanation of
                the emergency situation.</li>
            <li>Maternity/Paternity Leave: For maternity and paternity leave, employees must submit the appropriate
                certificates or documentation confirming the birth and expected recovery or bonding period.</li>
            <li>Special Leave: For special leave types (e.g., marriage, bereavement), employees must provide supporting
                documents as requested by HR.</li>
        </ul>

        <h4>Unpaid Leave</h4>
        <p>In cases where an employee has exhausted all available leave credits or does not have sufficient leave
            balance, they may request unpaid leave. The Company reserves the right to approve or deny unpaid leave
            requests based on operational needs.</p>

        <h4>Leave Records and Tracking</h4>
        <p>All leave requests and usage will be tracked and recorded in the HRMS. Employees can view their leave
            balances, request history, and status of pending requests at any time. The HR department will update leave
            balances accordingly and ensure that all leave data is accurately reflected.</p>

        <h4>Abuse of Leave</h4>
        <p>Any abuse of the leave policy, such as submitting false or misleading leave requests or misusing leave, may
            result in disciplinary action, including verbal or written warnings, suspension, or termination of
            employment, depending on the severity of the violation.</p>

        <h4>Leave Encashment</h4>
        <ul>
            <li>Vacation Leave: All unused vacation leave at the end of the year will be forfeited and will not be
                carried over to the following year. The leave balance will reset on the first day of the new calendar
                year.</li>
            <li>Other Leave Types: Sick leave and emergency leave cannot be encashed or carried over. Unused sick leave
                and emergency leave will be forfeited and reset at the end of each year.</li>
        </ul>

        <h4>Policy Amendments</h4>
        <p>The Company reserves the right to amend, modify, or update this policy at any time. Employees will be
            notified of any changes through official communications or HRMS notifications.</p>

