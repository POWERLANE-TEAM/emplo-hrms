        <h4>Purpose</h4>
        <p>This policy establishes the guidelines for retaining employee data post-separation to ensure compliance with
            applicable laws and to protect both the employee and the companyâ€™s interests.</p>

        <h4>Retention Period</h4>
        <p>Powerlane Resources Inc. retains employee data for a period of four (4) years following an employee's
            resignation or separation from the company. This includes personal information, performance records, and any
            other relevant documentation.</p>

        <h4>Access to Data</h4>
        <p>Employees who have separated from the company will continue to have access to their account and related data
            for the full four-year retention period. This access will remain available for reviewing past records,
            performance evaluations, and other historical data as needed.</p>

        <h4>Purpose of Retention</h4>
        <p>The retention of employee data is necessary for various reasons, such as:</p>
        <ul>
            <li>Legal compliance, particularly for tax, benefits, and social security purposes.</li>
            <li>For any future reference related to previous employment or disputes.</li>
            <li>To comply with potential future audits or legal requirements.</li>
        </ul>

        <h4>Data Deletion</h4>
        <p>After the retention period of four (4) years, all data related to the employee will be securely deleted from
            the system to comply with privacy and data protection standards.</p>

        <h4>Compliance with Legal Requirements</h4>
        <ul>
            <li><strong>Data Privacy Act of 2012 (Republic Act No. 10173):</strong> Powerlane Resources Inc. ensures
                that all employee data is handled, stored, and retained in compliance with the Data Privacy Act, which
                governs the lawful processing and retention of personal data.</li>
            <li><strong>Labor Code Compliance:</strong> The retention of employee data for record-keeping purposes
                complies with the Labor Code of the Philippines and its provisions concerning documentation of
                employment.</li>
        </ul>

        <h4>Employee Rights</h4>
        <p>Employees have the right to request the correction or deletion of inaccurate data in accordance with the Data
            Privacy Act. Such requests should be directed to the HR department for proper handling.</p>
