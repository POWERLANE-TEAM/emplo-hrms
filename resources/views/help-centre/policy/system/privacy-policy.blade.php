        <h4>Introduction</h4>
        <p>Powerlane Resources, Inc. (“we,” “us,” or “our”) is committed to protecting the privacy of all individuals,
            including job applicants, and recognizes its responsibility to manage personal data in accordance with
            Republic Act No. 10173, the Data Privacy Act of 2012 (DPA). This Privacy Policy provides a detailed overview
            of how we collect, process, protect, and manage the personal data of applicants within our Human Resource
            Management System (HRMS). This Privacy Policy applies to all individuals applying for employment with
            Powerlane Resources, Inc. through the HRMS.</p>

        <h4>Purpose of the Privacy Policy</h4>
        <p>This policy aims to:</p>
        <ul>
            <li>Demonstrate compliance with the DPA and related NPC regulations.</li>
            <li>Ensure transparency in how we collect, use, and share applicant personal data.</li>
            <li>Outline the rights of applicants regarding their personal data.</li>
        </ul>

        <h4>Scope and Applicability</h4>
        <p>This Privacy Policy covers all data processing activities related to applicants, including but not limited
            to:</p>
        <ul>
            <li>Application submission and recruitment processes.</li>
            <li>Evaluation of applications and resumes.</li>
            <li>Communication with applicants regarding job opportunities and application status.</li>
        </ul>

        <h4>Data We Collect</h4>
        <p>We collect and process the following types of personal data:</p>
        <ul>
            <li><strong>Personal Information</strong> - Full name, contact details, address, email, and phone number.
                Government-issued identification (e.g., SSS, TIN, PhilHealth).</li>
            <li><strong>Sensitive Personal Information</strong> - Biometric data (e.g., fingerprints, if applicable
                during interviews or assessments). Health information (if voluntarily provided by the applicant for
                specific roles or assessments).</li>
            <li><strong>Application Data</strong> - Resume/Curriculum Vitae (CV), cover letters, and other application
                documents. Job preferences and qualifications.</li>
            <li><strong>Machine Learning Data</strong> - Anonymized datasets used for training the HRMS's Resume
                Evaluator feature (e.g., scores, F1 evaluations).</li>
        </ul>

        <h4>Legal Basis for Processing</h4>
        <p>We process applicant data based on the following lawful criteria under the DPA:</p>
        <ul>
            <li><strong>Consent</strong>: Explicit consent is obtained during the application process.</li>
            <li><strong>Contractual Obligations</strong>: Data is necessary to fulfill pre-employment procedures.</li>
            <li><strong>Legitimate Interests</strong>: Enhancing the recruitment process and ensuring compliance with
                organizational policies.</li>
        </ul>

        <h4>How We Use the Data</h4>
        <p>We process applicant data to:</p>
        <ul>
            <li>Assess and evaluate job applications and resumes.</li>
            <li>Communicate with applicants regarding their application status and interview scheduling.</li>
            <li>Improve recruitment processes and HR decision-making.</li>
            <li>Verify the accuracy of applicant-provided information during background checks.</li>
        </ul>

        <h4>Data Sharing and Disclosure</h4>
        <p>We do not sell or share applicant data with third parties without explicit consent, except under the
            following circumstances:</p>
        <ul>
            <li><strong>Legal Compliance</strong>: Data may be shared with government agencies (e.g., BIR, SSS) for
                compliance purposes if required during the hiring process.</li>
            <li><strong>Third-Party Processors</strong>: Vendors (e.g., Google Document AI) who assist in HR operations
                are contractually bound to comply with our privacy standards.</li>
        </ul>

        <h4>Data Retention Policy</h4>
        <p>Personal data is retained only as long as necessary to:</p>
        <ul>
            <li>Fulfill the purposes for which it was collected (e.g., completing the recruitment process).</li>
            <li>Comply with legal obligations.</li>
        </ul>
        <p>Data no longer needed will be securely destroyed or anonymized in accordance with the NPC’s guidelines.</p>

        <h4>Data Security Measures</h4>
        <p>To ensure the confidentiality, integrity, and availability of personal data, we implement:</p>
        <ul>
            <li><strong>Technical Measures</strong>: Data encryption (in transit and at rest), firewalls, intrusion
                detection systems, and regular vulnerability assessments.</li>
            <li><strong>Organizational Measures</strong>: Regular training for personnel on data privacy, appointment of
                a Data Protection Officer (DPO).</li>
            <li><strong>Physical Measures</strong>: Restricted access to data storage facilities, secure disposal of
                printed records.</li>
        </ul>

        <h4>Applicant Rights Under the DPA</h4>
        <p>As an applicant, you have the following rights:</p>
        <ul>
            <li><strong>Right to Be Informed</strong>: Know why and how your data is being processed.</li>
            <li><strong>Right to Access</strong>: Request copies of your personal data.</li>
            <li><strong>Right to Rectification</strong>: Correct inaccuracies in your data.</li>
            <li><strong>Right to Erasure</strong>: Request deletion of data no longer necessary or if you withdraw your
                application.</li>
            <li><strong>Right to Data Portability</strong>: Obtain a copy of your data in a portable format, if
                applicable.</li>
            <li><strong>Right to Object</strong>: Decline specific data processing activities.</li>
        </ul>

        <h4>Privacy Impact Assessment (PIA)</h4>
        <p>We periodically conduct Privacy Impact Assessments (PIAs) to evaluate potential risks associated with
            applicant data processing activities. The PIA ensures that vulnerabilities are identified and mitigating
            measures are implemented.</p>

        <h4>Mitigation and Contingency Plans</h4>
        <p><strong>Mitigation</strong>: Proactive measures such as permission-based access control, automated threat
            detection, and regular patching are in place.</p>
        <p><strong>Contingency</strong>: In the event of a breach, we will notify affected applicants and the National
            Privacy Commission within the required time frame.</p>

        <h4>Changes to This Policy</h4>
        <p>This Privacy Policy may be updated periodically. Applicants will be informed of significant changes through
            system notifications or official communication channels.</p>

